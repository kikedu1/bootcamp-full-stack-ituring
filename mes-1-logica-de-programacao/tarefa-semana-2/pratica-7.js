function language(x) {
    if (x == "es") {
        console.log('¡Hola, Mundo!');
    } else if (x == "pt") {
        console.log('Olá, Mundo!');
    } else if (x == "en") {
        console.log('Hello, World!');
    } else  {
        console.log('Olá, Mundo!');
    }
   
}

language("es")
language("pt")
language("en")
language("it")
